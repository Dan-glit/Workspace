// Definir variables
let a = 10;
let b = 3;

// Operaciones
let suma = a + b;
let resta = a - b;
let multiplicacion = a * b;
let division = b !== 0 ? a / b : "No se puede dividir por cero";
let modulo = b !== 0 ? a % b : "No se puede calcular módulo con cero";
let potencia = a ** b;

// Resultados
console.log("Suma:", suma);
console.log("Resta:", resta);
console.log("Multiplicación:", multiplicacion);
console.log("División:", division);
console.log("Módulo:", modulo);
console.log("Potencia:", potencia);